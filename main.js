function detectLanguage() {
  const stored = localStorage.getItem('preferred-language');
  if (stored) return stored;

  const browserLang = navigator.language || navigator.userLanguage;
  return browserLang.toLowerCase().startsWith('de') ? 'de' : 'en';
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('preferred-language', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-lang]').forEach(el => {
    const key = el.getAttribute('data-lang');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll('[data-lang-btn]').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang-btn') === lang);
  });
}

function initLanguageSwitcher() {
  document.querySelectorAll('[data-lang-btn]').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang-btn'));
    });
  });
}

function handleHeaderScroll() {
  const header = document.getElementById('header');
  if (!header) return;

  if (window.scrollY > 50) {
    header.classList.add('scrolled');
  } else {
    header.classList.remove('scrolled');
  }
}

function initMobileNav() {
  const navToggle = document.getElementById('navToggle');
  const navWrapper = document.getElementById('navWrapper');
  const navLinks = document.querySelectorAll('.nav-link');
  const mobileNavClose = document.getElementById('mobileNavClose');

  if (!navToggle || !navWrapper) return;

  const closeNav = () => {
    navToggle.setAttribute('aria-expanded', 'false');
    navWrapper.classList.remove('active');
    document.body.style.overflow = '';
  };

  navToggle.addEventListener('click', () => {
    const isExpanded = navToggle.getAttribute('aria-expanded') === 'true';
    navToggle.setAttribute('aria-expanded', !isExpanded);
    navWrapper.classList.toggle('active');
    document.body.style.overflow = isExpanded ? '' : 'hidden';
  });

  if (mobileNavClose) {
    mobileNavClose.addEventListener('click', closeNav);
  }

  navLinks.forEach(link => {
    link.addEventListener('click', closeNav);
  });

  window.addEventListener('resize', () => {
    if (window.innerWidth > 992) {
      closeNav();
    }
  });
}

function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(link => {
    link.addEventListener('click', (e) => {
      const href = link.getAttribute('href');
      if (href === '#') return;

      const target = document.querySelector(href);
      if (target) {
        e.preventDefault();
        const headerHeight = document.getElementById('header')?.offsetHeight || 80;
        const targetPosition = target.offsetTop - headerHeight;

        window.scrollTo({
          top: targetPosition,
          behavior: 'smooth'
        });
      }
    });
  });
}

function updateActiveNavLink() {
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('.nav-link');
  const headerHeight = 100;

  let currentSection = '';

  sections.forEach(section => {
    const sectionTop = section.offsetTop - headerHeight - 100;
    const sectionHeight = section.offsetHeight;

    if (window.scrollY >= sectionTop && window.scrollY < sectionTop + sectionHeight) {
      currentSection = section.getAttribute('id');
    }
  });

  navLinks.forEach(link => {
    link.classList.remove('active');
    if (link.getAttribute('href') === `#${currentSection}`) {
      link.classList.add('active');
    }
  });
}

let currentImageIndex = 0;
let galleryImages = [];

function initImageLightbox() {
  const modal = document.getElementById('imageModal');
  const modalImage = document.getElementById('modalImage');
  const modalClose = document.getElementById('modalClose');
  const modalPrev = document.getElementById('modalPrev');
  const modalNext = document.getElementById('modalNext');
  const galleryItems = document.querySelectorAll('.gallery-item, .gallery-item-full');

  if (!modal || !modalImage) return;

  galleryImages = Array.from(galleryItems).map(item => {
    const img = item.querySelector('img');
    return {
      src: img.src,
      alt: img.alt
    };
  });

  galleryItems.forEach((item, index) => {
    const zoomBtn = item.querySelector('.gallery-zoom-btn');
    if (zoomBtn) {
      zoomBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        openLightbox(index);
      });
    }

    item.addEventListener('click', () => {
      openLightbox(index);
    });
  });

  if (modalClose) {
    modalClose.addEventListener('click', closeLightbox);
  }

  if (modalPrev) {
    modalPrev.addEventListener('click', showPrevImage);
  }

  if (modalNext) {
    modalNext.addEventListener('click', showNextImage);
  }

  modal.addEventListener('click', (e) => {
    if (e.target === modal) {
      closeLightbox();
    }
  });

  document.addEventListener('keydown', (e) => {
    if (!modal.classList.contains('active')) return;

    if (e.key === 'Escape') {
      closeLightbox();
    } else if (e.key === 'ArrowLeft') {
      showPrevImage();
    } else if (e.key === 'ArrowRight') {
      showNextImage();
    }
  });
}

function openLightbox(index) {
  const modal = document.getElementById('imageModal');
  const modalImage = document.getElementById('modalImage');

  if (!modal || !modalImage || !galleryImages[index]) return;

  currentImageIndex = index;
  modalImage.src = galleryImages[index].src;
  modalImage.alt = galleryImages[index].alt;
  modal.classList.add('active');
  document.body.style.overflow = 'hidden';

  setTimeout(() => {
    if (typeof lucide !== 'undefined' && lucide.createIcons) {
      lucide.createIcons();
    }
  }, 100);
}

function closeLightbox() {
  const modal = document.getElementById('imageModal');
  if (!modal) return;

  modal.classList.remove('active');
  document.body.style.overflow = '';
}

function showPrevImage() {
  currentImageIndex = (currentImageIndex - 1 + galleryImages.length) % galleryImages.length;
  const modalImage = document.getElementById('modalImage');
  if (modalImage && galleryImages[currentImageIndex]) {
    modalImage.src = galleryImages[currentImageIndex].src;
    modalImage.alt = galleryImages[currentImageIndex].alt;
  }
}

function showNextImage() {
  currentImageIndex = (currentImageIndex + 1) % galleryImages.length;
  const modalImage = document.getElementById('modalImage');
  if (modalImage && galleryImages[currentImageIndex]) {
    modalImage.src = galleryImages[currentImageIndex].src;
    modalImage.alt = galleryImages[currentImageIndex].alt;
  }
}

function initGalleryFilters() {
  const filterBtns = document.querySelectorAll('.filter-btn');
  const galleryItems = document.querySelectorAll('.gallery-item-full');

  if (filterBtns.length === 0) return;

  filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const filter = btn.getAttribute('data-filter');

      filterBtns.forEach(b => b.classList.remove('active'));
      btn.classList.add('active');

      galleryItems.forEach(item => {
        const category = item.getAttribute('data-category');

        if (filter === 'all' || category === filter) {
          item.style.display = 'block';
        } else {
          item.style.display = 'none';
        }
      });

      setTimeout(() => {
        if (typeof lucide !== 'undefined' && lucide.createIcons) {
          lucide.createIcons();
        }
      }, 100);
    });
  });
}

function initLucideIcons() {
  if (typeof lucide !== 'undefined' && lucide.createIcons) {
    lucide.createIcons();
  }
}

let scrollTimeout;
function optimizedScrollHandler() {
  if (scrollTimeout) {
    window.cancelAnimationFrame(scrollTimeout);
  }

  scrollTimeout = window.requestAnimationFrame(() => {
    handleHeaderScroll();
    updateActiveNavLink();
  });
}

function init() {
  const initialLang = detectLanguage();
  setLanguage(initialLang);

  initMobileNav();
  initSmoothScroll();
  initLanguageSwitcher();
  initImageLightbox();
  initGalleryFilters();
  renderNewsArticles();

  handleHeaderScroll();
  updateActiveNavLink();

  window.addEventListener('scroll', optimizedScrollHandler, { passive: true });

  setTimeout(() => {
    initLucideIcons();
  }, 100);
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', init);
} else {
  init();
}

window.addEventListener('load', () => {
  initLucideIcons();
});
